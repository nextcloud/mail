import _ from 'lodash'

class IndefinableValue {
	constructor(value) {
		this.value = value
	}

	or(other) {
		if (_.isUndefined(this.value)) {
			return other
		} else {
			return this.value
		}
	}
}

export const value = value => new IndefinableValue(value)
